  `name` varchar(30) DEFAULT NULL,
  `userId` varchar(100) DEFAULT NULL,
  `changedDate` datetime NOT NULL,
  `departmentId` varchar(100) DEFAULT NULL,
  `departmentname` varchar(150) DEFAULT NULL,
  `updatedUserId` varchar(100) DEFAULT NULL,
  `updatedusername` varchar(100) DEFAULT NULL,
  `active` int(10) unsigned DEFAULT '1',
  `role` varchar(300) DEFAULT NULL,
  `desigId` varchar(100) DEFAULT NULL,
  `seatId` varchar(500) DEFAULT NULL,
  `action` varchar(30) DEFAULT NULL,
  `designationname` varchar(150) DEFAULT NULL,
  PRIMARY KEY (`Id`),
  KEY `userreport_ibfk_1` (`departmentId`)
) ENGINE=InnoDB AUTO_INCREMENT=176 DEFAULT CHARSET=utf8 ROW_FORMAT=COMPACT;
CREATE TABLE `userroles` (
  `id` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `userId` varchar(100) DEFAULT NULL,
  `roleId` int(10) unsigned DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `userId` (`userId`),
  KEY `roleId` (`roleId`),
  CONSTRAINT `userroles_ibfk_2` FOREIGN KEY (`roleId`) REFERENCES `role` (`roleId`)
) ENGINE=InnoDB AUTO_INCREMENT=101682 DEFAULT CHARSET=utf8 ROW_FORMAT=COMPACT;
