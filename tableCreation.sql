CREATE TABLE `watchlist` (
  `id` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `folderName` varchar(250) NOT NULL,
  `description` varchar(250) DEFAULT NULL,
  `seatId` varchar(100) NOT NULL,
  `seatName` varchar(150) DEFAULT NULL,
  `owner` tinyint(1) NOT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uni_folder_seat` (`folderName`,`seatId`),
  KEY `seat` (`seatId`)
) ENGINE=InnoDB AUTO_INCREMENT=19 DEFAULT CHARSET=utf8 COMMENT='User created watchlist for files';
CREATE TABLE `watchlistfile` (
  `id` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `watchListId` int(10) unsigned NOT NULL,
  `fileNo` varchar(100) NOT NULL,
  `description` varchar(500) DEFAULT NULL,
  `fileDisplayNo` varchar(100) NOT NULL,
  `subject` varchar(2000) NOT NULL,
  `sectionId` varchar(100) DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uc_file_watchlist` (`watchListId`,`fileNo`),
  KEY `file` (`fileNo`)
